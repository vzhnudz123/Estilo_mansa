const formatGalleryLabel = (path) => {
  const filename = path.split('/').pop() || '';
  return filename
    .replace(/\.[^.]+$/, '')
    .replace(/^WhatsApp Image\s*/, '')
    .replace(/^IMG_/, 'IMG ')
    .replace(/[_-]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
};

